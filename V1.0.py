def on_link(sender, app_data):
    if not dpg.does_item_exist("editor"):
        print("Link ignored: NodeEditor does not exist")
        return

    link_id = dpg.add_node_link(app_data[0], app_data[1], parent="editor")
    LINKS.add(link_id)

    output_port = app_data[0]
    input_port  = app_data[1]

    # Create the visual link
    link_id = dpg.add_node_link(output_port, input_port, parent="editor")

    # Find the nodes that own these ports
    src_node = PORT_REGISTRY[output_port]
    dst_node = PORT_REGISTRY[input_port]

    # Track the link so we can delete it later
    src_node.links.add(link_id)
    dst_node.links.add(link_id)

    # Track graph connections
    dst_node.connect(src_node)

def on_delink(sender, app_data):
    link_id = app_data
    LINKS.discard(link_id)

