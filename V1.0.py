def ensure_datapack_structure(base_folder, namespace):
    # pack.mcmeta
    mcmeta_path = os.path.join(base_folder, "pack.mcmeta")
    if not os.path.exists(mcmeta_path):
        with open(mcmeta_path, "w") as f:
            json.dump({
                "pack": {
                    "pack_format": 26,
                    "description": "Generated by Node Editor"
                }
            }, f, indent=2)

    # Required folders
    os.makedirs(os.path.join(base_folder, "data", namespace, "functions"), exist_ok=True)
    os.makedirs(os.path.join(base_folder, "data", "minecraft", "tags", "functions"), exist_ok=True)

    # load.json
    load_tag = os.path.join(base_folder, "data", "minecraft", "tags", "functions", "load.json")
    if not os.path.exists(load_tag):
        with open(load_tag, "w") as f:
            json.dump({"values": [f"{namespace}:load"]}, f, indent=2)

    # tick.json
    tick_tag = os.path.join(base_folder, "data", "minecraft", "tags", "functions", "tick.json")
    if not os.path.exists(tick_tag):
        with open(tick_tag, "w") as f:
            json.dump({"values": [f"{namespace}:tick"]}, f, indent=2)

