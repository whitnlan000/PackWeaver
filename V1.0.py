def build_graph():
    global DATAPACK_FOLDER

    if not DATAPACK_FOLDER:
        dpg.set_value("OutputBox", "Select a datapack folder first.")
        return

    ensure_datapack_structure(DATAPACK_FOLDER, "built_pack")
    ends = [n for n in NODE_REGISTRY.values() if isinstance(n, EndNode)]
    if not ends:
        dpg.set_value("OutputBox", "No End node found")
        return

    ends.sort(key=lambda n: dpg.get_item_pos(n.node_id)[1])
    try:
        output = "\n".join(end.build() for end in ends)
    except:
        print("Build failed")
        output = "Error"

    # Show in UI
    dpg.set_value("OutputBox", output)

    # Ask user which function to write to
    # (you can replace this with a dropdown later)
    target = "tick"  # or "load"

    # Ask for namespace (or auto-detect)
    namespace = "mynamespace"

    # Build full path
    func_path = os.path.join(
        DATAPACK_FOLDER,
        "data",
        namespace,
        "functions",
        f"{TARGET_FUNCTION}.mcfunction"
    )

    os.makedirs(os.path.dirname(func_path), exist_ok=True)
    with open(func_path, "w") as f:
        f.write(output)

    if TARGET_FUNCTION in ("tick", "load"):
        tag_path = os.path.join(
            DATAPACK_FOLDER,
            "data",
            "minecraft",
            "tags",
            "functions",
            f"{TARGET_FUNCTION}.json"
        )

        tag_value = f"{namespace}:{TARGET_FUNCTION}"

        if os.path.exists(tag_path):
            with open(tag_path, "r") as f:
                data = json.load(f)
        else:
            data = {"values": []}

        if tag_value not in data["values"]:
            data["values"].append(tag_value)

        with open(tag_path, "w") as f:
            json.dump(data, f, indent=2)

        os.makedirs(os.path.dirname(tag_path), exist_ok=True)

    # Ensure folders exist
    os.makedirs(os.path.dirname(func_path), exist_ok=True)

    # Write file
    if not output == "Error":
        with open(func_path, "w") as f:
            f.write(output)

        print("Wrote to:", func_path)
        if TARGET_FUNCTION not in ("tick", "load"):
            # no tag file needed
            pass

