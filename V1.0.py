def load_project(path):
    data = json.load(open(path))
    global TARGET_FUNCTION

    # Create nodes
    for n in data["nodes"]:
        node_class = LOADING_TYPES[n["type"]]
        node = node_class(pos=n["pos"])

        # FIX: ensure node is created inside the editor
        dpg.push_container_stack("editor")
        node.setup()
        dpg.pop_container_stack()

        node.load_params(n["params"])

    for link in data["links"]:
        out_port = link["from_port"]
        in_port = link["to_port"]

        link_id = dpg.add_node_link(out_port, in_port, parent="editor")

        # Track link in nodes
        src_node = PORT_REGISTRY[out_port]
        dst_node = PORT_REGISTRY[in_port]

        src_node.links.add(link_id)
        dst_node.links.add(link_id)

        dst_node.connect(src_node)

    TARGET_FUNCTION = data["project"]["target_function"]
    dpg.set_value("CustomFunctionName", TARGET_FUNCTION)
    TARGET_FUNCTION = data["project"]["target_function"]

