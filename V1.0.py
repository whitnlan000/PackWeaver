def serialize_links():
    links = []
    for node in NODE_REGISTRY.values():
        for link_id in node.links:
            info = dpg.get_item_configuration(link_id)
            links.append({
                "from_port": info["attr_1"],
                "to_port":   info["attr_2"]
            })
    return links


